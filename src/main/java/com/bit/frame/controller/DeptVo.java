package com.bit.frame.controller;

public class DeptVo {
	private int dempno, sal;
	private String dname;
	
	public int getDempno() {
		return dempno;
	}
	public void setDempno(int dempno) {
		this.dempno = dempno;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	

}
